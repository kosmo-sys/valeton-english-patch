    Write-Host @"
GP-100 PatchToEnglish v2
========================

USAGE:
    .\GP-100-PatchToEnglish-v2.ps1 -input <firmware> [-output <output>] [OPTIONS]

OPTIONS:
    -input <file>          Input firmware file (required)
    -output <file>         Output firmware file (default: <input>-PatchedToEnglish-v2.bin)
    -r <start> <end>    Custom hex region to patch (e.g., -r 0x27A000 0x27D000)
    --force-patch       Skip version validation
    --help              Show this help message
