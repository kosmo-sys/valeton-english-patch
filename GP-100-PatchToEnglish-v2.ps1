EXAMPLES:
    .\GP-100-PatchToEnglish-v2.ps1 -input "GP-100 Firmware V2.0.bin"
    .\GP-100-PatchToEnglish-v2.ps1 -input firm.bin -output patchfirm.bin --force-patch
